/*:
 ### Mobile Apps
 * **Native**
   * *Downloaded form app store*
   * *Written in native language*
   * *App lives on the actual device*
   * *Full access to platform APIs*
   * *Awesome Performance*
   * *Souce code only works on target platform*
   * *Can receive Push Notificatins*
   * *Run inside the operation Systems*
 * **Web**
   * *Website design for mobile*
   * *Sometime tries to mimic native design*
   * *Written in HTML, CSS, Javascript*
   * *Network Connection required*
   * *Extremely limited API access*
 * **Hybrid**
   * *Native layer with embedded HTML*
   * *Basic access to native APIs*
   * *Apps lives on the actual devices*
   * *Written in HTML, CSS, Javascript*
 */
